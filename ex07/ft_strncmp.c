int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int k;

	k = 0;
	while (*s1 && *s2 && *s1 == *s2 && k < n)
	{
		s1++;
		s2++;
		k++;
	}
	return (*s1 - *s2);
}
